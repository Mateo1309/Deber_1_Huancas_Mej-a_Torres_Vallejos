using System;

namespace ComponentBasedProgramming.LibTrigonometricOperations
{
    public class CTrigonometricOperations
    {
        public static double SinFunction(double angle)
        {
            return Math.Sin(angle);
        }

        public static double CosFunction(double angle)
        {
            return Math.Cos(angle);
        }

        public static double TanFunction(double angle)
        {
            return Math.Tan(angle);
        }
    }
}
